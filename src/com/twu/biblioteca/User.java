package com.twu.biblioteca;

public class User {
    String name;
    String username;
    String password;
    String email;
    String phoneno;


    User(String name, String username, String password, String email, String phoneno){
        this.name = name;
        this.username = username;
        this.password = password;
        this.email = email;
        this.phoneno = phoneno;
    }


}
